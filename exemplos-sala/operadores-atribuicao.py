# atribuição simples
x = 5
print(x)

# adição na atribuição
x += 1
print(x)

# subtração na atribuição
subtracao = 5
subtracao -= 3
print(subtracao)

# multiplicação na atribuição
multiplicacao = 4
multiplicacao *= 3
print(multiplicacao)

# divisão na atribuição
divisao = 121
divisao /= 11
print(divisao)

# divisão inteira na atribuição
divisao_inteira = 10
divisao_inteira //=3
print(divisao_inteira)

# módulo na atribuição
modulo = 10
modulo %= 3
print(modulo)

# exponenciação na atribuição
exponenciacao = 5
exponenciacao **= 2
print(exponenciacao)